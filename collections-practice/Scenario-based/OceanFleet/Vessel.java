public class Vessel {
    String vesselId;
    String vesselName;
    String vesselType;
    double averageSpeed;

    public Vessel(String vesselId, String vesselName, String vesselType, double averageSpeed) {
        this.vesselId = vesselId;
        this.vesselName = vesselName;
        this.vesselType = vesselType;
        this.averageSpeed = averageSpeed;
    }

}
